public class C2Nr {
    private int a;
    private int b;

    public C2Nr(int a, int b){
        this.a = a;
        this.b = b;
    }

    public int getSum(){
        return a+b;
    }

    public int getA(){
        return a;
    }

    public int getB(){
        return b;
    }
}
